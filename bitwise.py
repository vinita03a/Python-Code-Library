# &      (1&1 -> 1 , otherwise 0)

a = 10
b = 6

print(a&b)

'''
            2^5   2^4   2^3  2^2   2^1  2^0
            32    16     8     4    2    1
10 ->                    1     0    1    0
6  ->                    0     1    1    0
-----------------------------------------------
10&6 ->                  0     0     1    0

'''

#  | (or)       (0|0 -> 0 , otherwise 1)

a = 10
b = 6
print(a|b)

'''
            2^5   2^4   2^3  2^2   2^1  2^0
            32    16     8     4    2    1
10 ->                    1     0    1    0
6  ->                    0     1    1    0
-----------------------------------------------
10|6 ->                  1     1     1    0
'''

